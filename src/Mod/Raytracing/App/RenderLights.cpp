#include "PreCompiled.h"
#ifndef _PreComp_
#endif

#include "RenderLights.h"

using namespace Raytracing;

RenderLight::RenderLight()
{
    Color[0] = 1.f;
    Color[1] = 1.f;
    Color[2] = 1.f;

    Power = 100.;
}

RenderLight::~RenderLight()
{
}

void RenderLight::setColor(int r, int g, int b)
{
    Color[0] = (float) r / 255;
    Color[1] = (float) g / 255;
    Color[2] = (float) b / 255;
}
